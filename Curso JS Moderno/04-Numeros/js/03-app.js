// Math Objeto

let resultado;

resultado = Math.PI;
console.log(resultado);
// Redondear
resultado = Math.round(2.9);
console.log(resultado);
// Redondear hacia arriba
resultado = Math.ceil(2.1);
console.log(resultado);
// Redondear hacia abajo
resultado = Math.floor(2.9);
console.log(resultado);
// Raiz cuadrada
resultado = Math.sqrt(144);
console.log(resultado);
// Potencia
resultado = Math.pow(2, 4);
console.log(resultado);
// Min
resultado = Math.min(3, 5, 1, 8, 4);
console.log(resultado);
// Max
resultado = Math.max(3, 5, 1, 8, 4);
console.log(resultado);
// Random
resultado = Math.random() * 10;
console.log(resultado);	
// Absoluto
resultado = Math.abs(-200);
console.log(resultado);
// Aleatorio dentro de un rango
// Puedes usarlo para una rifa
resultado = Math.floor(Math.random() * (50 - 10 + 1) + 10);
console.log(resultado);
